package com.ercansencan.zevk_isifre;

public class Sifreler {

	private String  veri;
    private String tarih;
 
    public Sifreler(String veri, String tarih) {
        super();
        this.veri = veri;
        this.tarih = tarih;
    }

	public String getVeri() {
		return veri;
	}

	public String getTarih() {
		return tarih;
	}
 
  
}
